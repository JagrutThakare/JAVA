package operators;

public class Comparison {// also called Relational Operator.
	public static void main(String[] args) {
		
		int a = 3;
		int b = 7;
		
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b); // not equal to operator.
		
	}

}
