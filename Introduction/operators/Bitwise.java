package operators;

public class Bitwise {

	public static void main(String[] args) {
		// 
		
		int a = 10;
		int b = 13;
		int c = a&b;
		int d = a|b;
		int e = a >> 1;
		int f = b << 1;
		int g = a ^ 1;
		int h = a >>> 1;
		
		System.out.println(c + " " + d + " " + e + " " + " " + f + " " + g + " " + h);

		
		
		
	}

}
